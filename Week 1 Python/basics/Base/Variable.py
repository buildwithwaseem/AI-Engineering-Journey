## VARIABLES
# A variable is a container for storing data values. In Python, you can create a variable and assign it a value using the assignment operator (=). The variable name can be any valid identifier, and the value can be of any data type (e.g., string, integer, float, etc.).

name = "waseem"
age = 25
print(name)
print(age)


