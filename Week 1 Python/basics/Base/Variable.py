'''Changing variables'''
# You can change the value of a variable by assigning a new value to it. For example:``

# Start with one value
score = 0
print(score)  # Shows: 0

# Change it
score = 10
print(score)  # Shows: 10

# Change it again
score = score + 5
print(score)  # Shows: 15







