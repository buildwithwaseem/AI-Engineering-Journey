# PYTHON NAMING CONVENTION
# In Python, the convention for naming variables is to use lowercase letters and separate words with underscores (also known as snake_case). This makes variable names more readable and consistent with the Python community's style guidelines.
# For example:

# Good Python style
first_name = "Waasu"
user_age = 25
is_logged_in = True
shopping_cart_total = 49.99

# Avoid camelCase (this is for other languages)
